/**
 * Fix Production Environment Configuration
 * 
 * This script updates the production environment file with all necessary
 * environment variables for email functionality and other services.
 */

import fs from 'fs';
import dotenv from 'dotenv';

// Load current environment to get values
dotenv.config();

async function fixProductionEnvironment() {
  console.log('🔧 FIXING PRODUCTION ENVIRONMENT CONFIGURATION');
  console.log('==============================================\n');

  // Check current secrets availability
  const requiredSecrets = {
    'SENDGRID_API_KEY': process.env.SENDGRID_API_KEY,
    'DEFAULT_FROM_EMAIL': process.env.DEFAULT_FROM_EMAIL || 'support@matchpro.ai',
    'SESSION_SECRET': process.env.SESSION_SECRET,
    'STRIPE_SECRET_KEY': process.env.STRIPE_SECRET_KEY,
    'STRIPE_PUBLISHABLE_KEY': process.env.STRIPE_PUBLISHABLE_KEY,
    'STRIPE_CONNECT_CLIENT_ID': process.env.STRIPE_CONNECT_CLIENT_ID,
    'FRONTEND_URL': process.env.FRONTEND_URL || 'https://matchpro.ai',
    'BACKEND_URL': process.env.BACKEND_URL || 'https://matchpro.ai'
  };

  console.log('1. Checking available secrets:');
  console.log('==============================');
  
  const availableSecrets = {};
  const missingSecrets = [];
  
  Object.entries(requiredSecrets).forEach(([key, value]) => {
    if (value && value.trim()) {
      availableSecrets[key] = value;
      console.log(`✅ ${key}: Available`);
    } else {
      missingSecrets.push(key);
      console.log(`❌ ${key}: Missing`);
    }
  });

  // Create updated production environment file
  const currentProdConfig = {
    'NODE_ENV': 'production',
    'PORT': '5000',
    'VITE_API_URL': '',
    'DATABASE_URL': 'postgres://postgres:password@localhost:5432/postgres'
  };

  console.log('\n2. Creating updated .env.production file:');
  console.log('=========================================');

  const newProdConfig = {
    ...currentProdConfig,
    ...availableSecrets
  };

  let envContent = '# Production Environment Configuration\n';
  envContent += '# Generated by fix-production-environment.js\n\n';

  // Core application settings
  envContent += '# Core Application\n';
  envContent += `NODE_ENV=${newProdConfig.NODE_ENV}\n`;
  envContent += `PORT=${newProdConfig.PORT}\n`;
  envContent += `VITE_API_URL=${newProdConfig.VITE_API_URL}\n\n`;

  // Database
  envContent += '# Database\n';
  envContent += `DATABASE_URL=${newProdConfig.DATABASE_URL}\n\n`;

  // Email Configuration
  envContent += '# Email Configuration\n';
  if (newProdConfig.SENDGRID_API_KEY) {
    envContent += `SENDGRID_API_KEY=${newProdConfig.SENDGRID_API_KEY}\n`;
  } else {
    envContent += '# SENDGRID_API_KEY=your_sendgrid_api_key_here\n';
  }
  envContent += `DEFAULT_FROM_EMAIL=${newProdConfig.DEFAULT_FROM_EMAIL}\n\n`;

  // Authentication
  envContent += '# Authentication\n';
  if (newProdConfig.SESSION_SECRET) {
    envContent += `SESSION_SECRET=${newProdConfig.SESSION_SECRET}\n`;
  } else {
    envContent += '# SESSION_SECRET=your_session_secret_here\n';
  }
  envContent += '\n';

  // Stripe Configuration
  envContent += '# Stripe Configuration\n';
  if (newProdConfig.STRIPE_SECRET_KEY) {
    envContent += `STRIPE_SECRET_KEY=${newProdConfig.STRIPE_SECRET_KEY}\n`;
  } else {
    envContent += '# STRIPE_SECRET_KEY=your_stripe_secret_key_here\n';
  }
  if (newProdConfig.STRIPE_PUBLISHABLE_KEY) {
    envContent += `STRIPE_PUBLISHABLE_KEY=${newProdConfig.STRIPE_PUBLISHABLE_KEY}\n`;
  } else {
    envContent += '# STRIPE_PUBLISHABLE_KEY=your_stripe_publishable_key_here\n';
  }
  if (newProdConfig.STRIPE_CONNECT_CLIENT_ID) {
    envContent += `STRIPE_CONNECT_CLIENT_ID=${newProdConfig.STRIPE_CONNECT_CLIENT_ID}\n`;
  } else {
    envContent += '# STRIPE_CONNECT_CLIENT_ID=your_stripe_connect_client_id_here\n';
  }
  envContent += '\n';

  // URLs
  envContent += '# Application URLs\n';
  envContent += `FRONTEND_URL=${newProdConfig.FRONTEND_URL}\n`;
  envContent += `BACKEND_URL=${newProdConfig.BACKEND_URL}\n\n`;

  // Write the updated file
  try {
    fs.writeFileSync('.env.production', envContent);
    console.log('✅ Updated .env.production file successfully');
  } catch (error) {
    console.log('❌ Failed to write .env.production file:', error.message);
    return;
  }

  console.log('\n3. Production environment status:');
  console.log('=================================');

  if (missingSecrets.length === 0) {
    console.log('✅ All required secrets are available');
    console.log('✅ Production environment is fully configured');
  } else {
    console.log('⚠️  Some secrets are still missing:');
    missingSecrets.forEach(secret => {
      console.log(`   • ${secret}`);
    });
    console.log('\n📝 Next steps:');
    console.log('1. Add the missing secrets to your Replit environment');
    console.log('2. Re-run this script to update .env.production');
    console.log('3. Deploy to production');
  }

  console.log('\n4. Summary:');
  console.log('===========');
  console.log('The production environment file has been updated with available secrets.');
  console.log('Email functionality should now work in production if SENDGRID_API_KEY is available.');
  
  if (!availableSecrets.SENDGRID_API_KEY) {
    console.log('\n🚨 CRITICAL: SENDGRID_API_KEY is still missing');
    console.log('Password reset emails will continue to fail until this is added.');
  } else {
    console.log('\n✅ SENDGRID_API_KEY is configured - emails should work in production');
  }
}

fixProductionEnvironment().catch(console.error);