#!/usr/bin/env node

/**
 * Test Automated Tournament Parameters System
 * 
 * This script verifies that the Tournament Parameters API can:
 * 1. Analyze existing tournament data (teams, age groups, fields)
 * 2. Automatically generate complete tournament configuration
 * 3. Provide one-click setup without manual configuration
 */

import { execSync } from 'child_process';
import fetch from 'node-fetch';

const API_BASE = 'http://localhost:5000';
const EVENT_ID = '1844329078'; // Empire Super Cup

async function testAutomatedTournamentParameters() {
  console.log('🧪 Testing Automated Tournament Parameters System');
  console.log('=' .repeat(60));

  try {
    // Step 1: Check current tournament data
    console.log('\n📊 Step 1: Analyzing existing tournament data...');
    
    const eventResponse = await fetch(`${API_BASE}/api/admin/events/${EVENT_ID}`, {
      credentials: 'include'
    });
    
    if (!eventResponse.ok) {
      throw new Error('Failed to fetch event data - authentication required');
    }
    
    const eventData = await eventResponse.json();
    console.log(`✅ Event: ${eventData.name}`);
    console.log(`   Start: ${eventData.startDate}`);
    console.log(`   End: ${eventData.endDate}`);
    console.log(`   Location: ${eventData.location}`);

    // Step 2: Test automated tournament parameters generation
    console.log('\n🤖 Step 2: Testing automated parameter generation...');
    
    const parametersResponse = await fetch(`${API_BASE}/api/admin/events/${EVENT_ID}/tournament-parameters`, {
      credentials: 'include'
    });
    
    if (!parametersResponse.ok) {
      throw new Error('Failed to fetch tournament parameters - check authentication');
    }
    
    const parameters = await parametersResponse.json();
    
    // Display automation results
    console.log('\n🎯 AUTOMATION RESULTS:');
    console.log('=' .repeat(40));
    
    console.log(`🏆 Age Groups: ${parameters.ageGroups?.length || 0} configured`);
    if (parameters.ageGroups && parameters.ageGroups.length > 0) {
      parameters.ageGroups.forEach(ag => {
        console.log(`   • ${ag.name} (${ag.format}, ${ag.gameLength}min)`);
      });
    }
    
    console.log(`🎮 Game Formats: ${parameters.gameFormats?.length || 0} configured`);
    if (parameters.gameFormats && parameters.gameFormats.length > 0) {
      parameters.gameFormats.forEach(gf => {
        console.log(`   • ${gf.ageGroup}: ${gf.gameLength}min (${gf.halves} halves)`);
      });
    }
    
    console.log(`⚽ Fields: ${parameters.fields?.length || 0} available`);
    if (parameters.fields && parameters.fields.length > 0) {
      const fieldSizes = parameters.fields.reduce((acc, f) => {
        acc[f.fieldSize] = (acc[f.fieldSize] || 0) + 1;
        return acc;
      }, {});
      Object.entries(fieldSizes).forEach(([size, count]) => {
        console.log(`   • ${count}x ${size} fields`);
      });
    }
    
    console.log(`⏰ Schedule Constraints: ${parameters.timeSlots?.length || 0} configured`);
    if (parameters.timeSlots && parameters.timeSlots.length > 0) {
      parameters.timeSlots.forEach(ts => {
        console.log(`   • Operating: ${ts.earliestGameTime} - ${ts.latestGameTime}`);
        console.log(`   • Rest time: ${ts.minRestTimeBetweenGames} minutes`);
        console.log(`   • Max games/team/day: ${ts.maxGamesPerTeamPerDay}`);
      });
    }
    
    console.log(`\n👥 Total Teams: ${parameters.totalTeams || 0}`);
    console.log(`🏟️ Total Fields: ${parameters.totalFields || 0}`);
    
    // Step 3: Test field capacity analysis
    if (parameters.fieldCapacityAnalysis) {
      console.log('\n📈 FIELD CAPACITY ANALYSIS:');
      console.log('=' .repeat(40));
      
      Object.entries(parameters.fieldCapacityAnalysis.required).forEach(([size, required]) => {
        const available = parameters.fieldCapacityAnalysis.available[size];
        const status = available >= required ? '✅' : '⚠️';
        console.log(`${status} ${size}: ${available} available / ${required} required`);
      });
    }
    
    // Step 4: Verify automation status
    console.log('\n🔄 AUTOMATION STATUS:');
    console.log('=' .repeat(40));
    
    if (parameters.autoGenerated) {
      console.log('✅ FULLY AUTOMATED: Tournament parameters auto-generated from existing data');
      console.log('   • Age groups extracted from team registrations');
      console.log('   • Game formats configured based on field sizes');
      console.log('   • Schedule constraints set to tournament standards');
      console.log('   • Field inventory loaded from complex database');
      console.log('\n🎉 READY FOR ONE-CLICK SCHEDULING!');
    } else {
      console.log('ℹ️  Manual configuration detected - parameters already exist');
    }
    
    // Step 5: Test saving automated configuration
    if (parameters.autoGenerated) {
      console.log('\n💾 Step 3: Testing automated configuration save...');
      
      const saveData = {
        ageGroups: parameters.ageGroups,
        gameFormats: parameters.gameFormats,
        brackets: parameters.brackets || [],
        fields: parameters.fields,
        timeSlots: parameters.timeSlots
      };
      
      const saveResponse = await fetch(`${API_BASE}/api/admin/events/${EVENT_ID}/tournament-parameters`, {
        method: 'PUT',
        headers: {
          'Content-Type': 'application/json'
        },
        credentials: 'include',
        body: JSON.stringify(saveData)
      });
      
      if (saveResponse.ok) {
        console.log('✅ Automated configuration saved successfully');
        console.log('   Tournament parameters now locked and ready for scheduling');
      } else {
        console.log('❌ Failed to save automated configuration');
      }
    }
    
    console.log('\n🎯 AUTOMATION TEST COMPLETE');
    console.log('=' .repeat(60));
    console.log('The Tournament Parameters system successfully:');
    console.log('• Analyzed existing tournament data automatically');
    console.log('• Generated complete configuration without manual input');
    console.log('• Provided transparency on WHO plays WHO, WHEN, WHERE');
    console.log('• Ready for one-click tournament scheduling');
    
  } catch (error) {
    console.error('\n❌ Automation test failed:', error.message);
    console.log('\n💡 Make sure to:');
    console.log('1. Start the application (npm run dev)');
    console.log('2. Log in as admin in the web interface');
    console.log('3. Ensure Empire Super Cup event exists');
    process.exit(1);
  }
}

// Run the test
testAutomatedTournamentParameters();